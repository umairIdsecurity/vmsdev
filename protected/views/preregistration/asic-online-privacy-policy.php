        <h3 class="text-primary subheading-size">Auscheck Privacy Policy</h3>
        <div class="bg-gray-lighter privacy-notice" style="height:360px !important;">
		<div class="privacy-info text-size">
			<p>AusCheck coordinates background checks for the Aviation Security Identification Card (ASIC), Maritime Security Identification Card (MSIC) and National Health Security (NHS) schemes by using the information you provide in your application to ask the following government agencies for information about you:</p>
<ul>
<li><strong><a href="http://www.asio.gov.au/">Australian Security Intelligence Agency (ASIO)</a></strong>: ASIO will check your name on a database of known persons involved with politically motivated violence. ASIO will also keep your information and use it as necessary for national security purposes.</li>
<li><strong><a href="http://www.acic.gov.au/">Australian Criminal Intelligence Commission (ACIC)</a></strong>: If you are over 18, ACIC will check your criminal history in the databases of all Australian legal jurisdictions and supply a copy of your criminal record to AusCheck. ACIC will not use your information for any other purpose.</li></ul>
<p>If requested, issuing bodies for the ASIC and MSIC schemes may also request that your information be sent to:</p>

<ul>
<li><strong><a href="http://www.border.gov.au/">Department of Immigration and Border Protection (DIBP)</a>: </strong>DIBP will check your citizenship status or your legal right to work in Australia. DIBP may also use your information for immigration compliance purposes.</li>
</ul>
<p><strong>If you are under 18 years of age</strong>, only the national security component of the background check will be undertaken. AusCheck will disclose your personal information to ASIO in conducting this check. ASIO will check your name on a database of known persons involved with politically motivated violence. ASIO will also keep your information and use it as necessary for national security purposes. AusCheck will only provide your personal information for other purposes where specifically required or permitted by law. </p>
<p><strong>If you are under 14 years of age,</strong> your parent or legal guardian will be required to provide written consent to your Issuing Body for the background check to be undertaken. </p>
<h2 class="h2">AusCheck’s legal authority</h2>
<p>The collection and use of your information is authorised under the AusCheck Regulations 2007, Aviation Transport Security Regulations 2005, Maritime Transport and Offshore Facilities Security Regulations 2003 and National Health Security Regulations 2008 (NHS background checks).</p>
<h2 class="h2">Why personal information being collected by AusCheck</h2>
<p>Your issuing body/entity will collect the information that AusCheck needs about you in order to correctly identify you and to conduct a background check. For example, supplying your past addresses helps ensure AusCheck can exclude criminal records that relate to a different person with the same name as you.</p>
<h2 class="h2">What personal information is required</h2>
<p>AusCheck will need information about:</p>
<ul>
<li><strong>identity: </strong>your full name, other names, date of birth, gender, your residential address and current contact details</li>
<li><strong>work</strong>: your employer contact details and issuing body/entity details if required for the ASIC and MSIC scheme </li>
<li><strong>immigration </strong>(only if an immigration check is requested by your ASIC or MSIC issuing body): date of arrival in Australia, port of arrival and other relevant detailst, such as your travel document or visa number, flight number or name of vessel and the full name of your parent if you entered Australia on your parent's passport. An NHS check does not require immigration information.</li>
</ul>
<p>Only your identity and immigration information are used during the background checking process. Your work information is stored in the AusCheck database for other purposes explained below. Your issuing body/entity will also need a photograph of you, which will be supplied to AusCheck for the database.</p>
<p>Any other personal information that your issuing body/entity asks for is for their own purposes, not for AusCheck. If you have any concerns, you should ask your issuing body/entity to explain why they need that extra information about you.</p>
<h2 class="h2">How AusCheck will use personal information</h2>
<p>AusCheck will use the results of your background check to advise your issuing body/entity whether you:</p>
 
<ul>
<li>have an adverse criminal history</li>
 
<li> have an adverse security assessment</li>
<li>are prevented by immigration reasons from accessing aviation or maritime facilities. for the ASIC and MSIC schemes.</li></ul>

<p>AusCheck will only reveal the information necessary for your issuing body or entity to decide whether or not to issue you with an ASIC or MSIC, or allow you to access a secure facility handling Security Sensitive Biological Agents (SSBAs).</p>
<p>It is important to note that: </p>
<ul>
<li>an ASIC or MSIC issuing body or NHS entity will not receive a copy of your security assessment</li>
<li>an MSIC issuing body will not receive a copy of your criminal history</li>
<li>an ASIC issuing body or NHS entity will not receive a copy of your criminal history, unless you receive a 'qualified' finding</li>
<li>ASIC and MSIC issuing bodies will be told about your immigration check results</li>
<li>the NHS check scheme does not issue cards.</li>
</ul>
<h2 class="h2">How AusCheck discloses personal information</h2>
<p>All information provided to AusCheck (including application information, checking partner information, and any additional information provided by the individual) forms part of the AusCheck database established under subsection 14(1) of the <em>AusCheck Act</em>. Your personal information will only be disclosed in accordance with the AusCheck Act 2007or another applicable law in the ways set out below, and would not ordinarily be disclosed overseas.</p>
<h2 class="h2">ASICs and MSICs</h2>
<p>Issuing bodies, and maritime industry participants authorised to issue temporary MSICs or that control access to secure areas have limited access to the AusCheck database. This is to allow them to check the validity of an ASIC or MSIC at any given time. Only the information that is visible on an ASIC or MSIC will be disclosed in these circumstances.</p>
<p>Only your issuing body can see all the other information that you supplied in your application, such as your date of birth and address.</p>

<h2 class="h2">AusCheck’s Privacy Notice</h2>

<p>The Privacy Notice explains what personal information is collected about you when you apply for an ASIC or MSIC, how your personal information will be used and where you can find out more information.</p>
<p>More information is available in the following downloads:</p>
<ul>

<li><a href="/CrimeAndCorruption/AusCheck/Documents/aus-check-privacy-notice.pdf">AusCheck ASIC MSIC Privacy Notice [PDF 177KB]</a></li>
<li><a href="/CrimeAndCorruption/AusCheck/Documents/aus-check-privacy-notice.docx">AusCheck ASIC MSIC Privacy Notice [DOCX 89KB] </a></li>

</ul>


<h2 class="h2">Transferring a NHS check result</h2>

<p>If you require authorisation at another SSBA facility and your most recent background check is less than two years old, you are able to consent to your findings being re-used if your background check produced an 'eligible' finding. However, if your background check produced a 'qualified' finding, your most recent background check must not be more than 12 months old. You must consent for your new entity to be advised on whether you have been convicted of a health-security-relevant offence and be provided with the details of your criminal history.</p>

<h2 class="h2">Australian Government authorities</h2>

<p>The AusCheck database may be accessed by some Commonwealth, state and territory authorities in certain circumstances. Any agency seeking to access the AusCheck database must satisfy AusCheck that they require this access for the purpose of performing functions relating to law enforcement or national security.</p>

<h2 class="h2">What personal information is on ASIC or MSIC</h2>

<p>An ASIC or MSIC indicates that the person named on the card has undergone a national security background check and a valid ASIC or MSIC has been issued. </p>

<p>An ASIC or MSIC includes the holder's name, colour photo, unique identifier card's expiry date. ASICs may also indicate what airport(s) the card is valid for.</p>

<p>Some maritime and aviation sites or employers may require you to use your ASIC or MSIC card like a key – to enter or exit a site, to 'clock in' at work, or to access tools. Cards used for these purposes may have extra features like magnetic strips on the back or computer chips inside them. These features, and any extra information they hold about you, are unrelated to the ASIC or MSIC schemes. AusCheck is not responsible for how your personal information is collected or used by maritime and aviation sites or employers.</p>

<h2 class="h2">How you can access or update personal information</h2>

<p>You have the right to access the personal information that AusCheck holds about you. You also have the right to ask for any changes needed to make your personal information correct, complete, and not misleading.</p>

<p>To make an access or correction request, contact Attorney-General's Department Privacy Contact Officer at privacy@ag.gov.au. </p>
<p>AusCheck cannot change your criminal record, ASIO security assessment or DIBP immigration assessment. If you believe the results of your criminal record check are incorrect, you need to apply for correction with the relevant police service. Challenges to your security assessment or immigration status should be made directly to ASIO or DIBP.</p>

<h2 class="h2">How AusCheck deals with spent convictions</h2>

<p>The aim of the spent convictions scheme is to prevent discrimination on the basis of old and minor criminal convictions for people who have had a clean record since that time.</p>

<p>If you have a spent conviction, you do not have to disclose that conviction to anyone when you are asked about your criminal record. Also, spent convictions will not show up in your criminal record check.</p>

<p>Your conviction will be considered a spent conviction if all of the following conditions are met:</p>

<ul>
<li>it is old—it is 10 years since the date of your conviction (or 5 years if you were a child at the time of your conviction)</li>
<li>it was minor—you were sentenced to less than 30 months (2½ years) imprisonment (or you were not imprisoned at all)</li>
<li>you have not re-offended during the 10 year waiting period (or 5 years if you were a child at the time of your conviction)</li>
<li>an exclusion does not apply. </li>
</ul>

<p>Spent convictions also include convictions that have been set aside or pardoned.</p>
<p>There are a few exclusions that apply. The details of any convictions for the offences listed below will still be used by AusCheck in its assessment of you.</p>
<p>The exclusions for ASIC applicants are:</p>
<ol type="1">
<li>Offences against Part 2 of the <em>Crimes (Aviation) Act 1991</em></li>
<li>Division 73, Chapter 5.3 of the <em>Criminal Code Act 1995</em></li></ol>
<p>The exclusions for MSIC applicants are:</p>
<ol type="1">
<li>Offences against Part 4 of the <em>Australian Passports Act 2005</em></li>
<li>Offences against section 24AA, 24AB, 24C, 24D, 25, 27 or 29 of the <em>Crimes Act 1914</em></li>
<li>Offences against Part 2 of the <em>Crimes (Aviation) Act 1991</em></li>
<li>Offences against Division 73, Chapter 5, Division 145 or Division 400 of the <em>Criminal Code Act 1995</em></li>
<li>Offences against section 233, 233A, 233AC, 233B, 233BAA or 233BAB of the <em>Customs Act 1901</em></li>
<li>Offences against Division 10 of Part IV of the <em>Navigation Act 1912</em></li>
<li>Offences against section 9, 10, 11 or 14 of the <em>Weapons of Mass Destruction (Prevention of Proliferation) Act 1995</em></li>
</ol>
<p>Convictions for health security relevant offences do not become spent if they fall within the scope of the National Health Security Check Scheme exemptions listed in Schedule 4 of the Crimes Regulations 1990.</p>
<p>All other 'spent convictions' are considered irrelevant and cannot prevent you from being eligible. If you believe the 'spent convictions' rules have been breached by AusCheck, you can apply to the Office of the Privacy Commissioner for an investigation.</p>
<h2 class="h2">How AusCheck secures personal information</h2>
<p>Your personal information is secure when it is entered, processed, transferred and stored by AusCheck. The AusCheck System is secured using a number of methods appropriate for secure government systems. The following list provides some details of the data security arrangements.</p>

<h3 class="h3">Authorisation</h3>

<p>Access is only granted to people who have been properly authorised to use the system. System access is granted to individuals, not entire organisations. Users must demonstrate their need to access the system.</p>

<p>Issuing bodies and entities can only gain access if they have been accredited by the Department of Infrastructure and Regional Development, or the Department of Health. Other government agencies may access the system in certain circumstances—this access is strictly limited and may only be granted subject to the agency meeting the requirements prescribed by legislation.</p>
<h3 class="h3">Authentication</h3>

<p>Access is only granted to people who can prove they are who they say they are. AusCheck uses best practice standards for user IDs and passwords, and requires digital certificates to access system-to-system connections.</p>

<h3 class="h3">Access control</h3>

<p>System functions can only be accessed by authorised parties in authorised ways. The system limits access to different functions and information for different users. Users are granted access only to particular data and functions necessary to do their job. All functions are secured by access control lists.</p>

<h3 class="h3">Transfer control</h3>

<p>When processing your application, information is sent to and from AusCheck’s checking partners through secure communication networks that are not publicly accessible. When in transit, information is encrypted using methods approved by the Defence Signals Directorate.</p>
<h3 class="h3">Intrusion protection</h3>

<p>The system uses hacking and intrusion protection measures. The databases and servers are hosted in a secure Attorney-General’s Department environment that exceeds the classification requirements of the AusCheck system.</p>

<h3 class="h3">Audit and logging</h3>

<p>All system activity is logged. The system maintains a non-repudiable record of activity to deter and detect unauthorised system activity. </p>

<h3 class="h3">Data destruction</h3>

<p>AusCheck only keeps your personal information for as long as it is needed, or where required by law to keep it. When it is no longer needed, your personal information will be deleted or destroyed securely.</p>

<h2 class="h2">Australian Privacy Principles</h2>

<p>AusCheck is bound by the Australian Privacy Principles (APPs) contained in the <em>Privacy Act 1988</em>. If you believe that AusCheck may have breached the APPs or you wish to make a privacy complaint, email the Attorney-General’s Department Privacy Contact Officer at <a href="mailto:privacy@ag.gov.au">privacy@ag.gov.au</a>. The Privacy Contact Officer will then discuss your complaint with you and will attempt to resolve the issue. If this is not possible you can make a formal privacy complaint and the Attorney-General's Department will initiate a review into the issue or alleged breach of the APPs. You will be provided with a formal response from the department to your complaint within 30 days. If this does not resolve your complaint, you may then contact the Office of the Australian Information Commissioner for further review on 1300 363 992.</p>

<p>You may complain directly to the Office of the Australian Information Commissioner on 1300 363 992 at any time, though it is likely they will request you use our complaints process before they review your complaint further.</p>

